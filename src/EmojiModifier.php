    public function makeEmojiAccessible($text)
    {
        $search = array_map(
            function ($value) {
                $parts = explode(' ', $value);
                $return = '';
                foreach ($parts as $part) {
                    $return .= '\x{' . substr($part, 2) . '}';
                }
                return '/' . $return . '/u';
            },
            array_keys($this->emoji)
        );
        $replacements = array_map(
            function ($key, $value) {
                return '<span role="img" aria-label="' . $value . '">' . $this->convertCodepoints($key) . '</span>';
            },
            array_keys($this->emoji),
            $this->emoji
        );

        return preg_replace(
            $search,
            $replacements,
            $text
        );
    }

    private function convertCodepoints($codepoints)
    {
        $return = '';
        $parts = explode(' ', $codepoints);
        foreach ($parts as $codepoint) {
            $num = hexdec(substr($codepoint, 2));
            if($num<=0x7F)       $return .= chr($num);
            if($num<=0x7FF)      $return .= chr(($num>>6)+192).chr(($num&63)+128);
            if($num<=0xFFFF)     $return .= chr(($num>>12)+224).chr((($num>>6)&63)+128).chr(($num&63)+128);
            if($num<=0x1FFFFF)   $return .= chr(($num>>18)+240).chr((($num>>12)&63)+128).chr((($num>>6)&63)+128).chr(($num&63)+128);
        }

        return $return;
    }
